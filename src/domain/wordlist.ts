/**
 * Picks a random word from the curated list, retrying (in a random order,
 * without repeats) while `collides` reports the candidate as taken. Fails with
 * WordlistExhausted only when every word collides.
 */
export const pickWord = Effect.fn("wordlist.pickWord")(function* (
  collides: (word: string) => boolean | Effect.Effect<boolean>,
) {
  const shuffled = yield* Random.shuffle(WORDS);
  for (const word of shuffled) {
    const result = collides(word);
    const collision = yield* Effect.isEffect(result)
      ? (result as Effect.Effect<boolean>)
      : Effect.succeed(result);
    if (!collision) {
      return word;
    }
  }
  return yield* new WordlistExhausted();
});
